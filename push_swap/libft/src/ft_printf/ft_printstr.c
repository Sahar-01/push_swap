#include "ft_printf.h"
#include <unistd.h>

int	ft_printstr(char *s)
{
	int		i;

	i = 0;
	if (s == NULL)
	{
		return (6);
	}
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
